                   <p class="lead">
                   <u style="font-size: 1.3em;color: green">3次元ボールミル(3次元リアクター)の優位点</u>
                   <br>
                    <br>
①3次元ボールミル(3次元リアクター)容器は公転中心。<br>
②粉砕能力が高く、省電力で効率的な運動。<br>
③乾式粉砕時に固形化が少ない。<br>
④粉砕時に発熱が少ない。<br>
⑤粘度や比重の異なる均一的な混合も可能。
(3D　リアクターとしての活用)<br>
⑥非臨界回転運動(非供回り運動)を実現<br><br>

高速3次元運動を活用した問題解決！<br>
①2次元運動の乾式粉砕時の固形化<br>
【Solution】<br>
・高速3次元運動により、乾式粉砕時の非固形化を実現。<br><br>

②2次元運動の粉砕時の発熱問題<br>
【Solution】<br>
・容器全体面を利用し、摩擦熱が容器全面に分散。よって摩擦熱が発生しにくい。有・無機物に有効。<br><br>

③2次元運動の粉砕時の粒度のバラつき<br>
【Solution】<br>
・高速3次元運動の摩擦力を生かしたバッチ式粉砕。粒径が丸く、均一的な粒度を実現。<br><br>

④2次元運動の混合ムラ。(1方向のみでは、ムラが発生)<br>
【Solution】<br>
・ペラ、羽根を不使用。高速3次元運動は容器全体面を高効率使用。低発熱、均一混合を達成<br><br>
</p>

