Since the container diameter is larger than Planet series, it is possible to crush a large amount.<br>
It is recommended for those who were satisfied with the crushing amount of the planetary ball mill.<br>
Furthermore, since the surface of the milling container is used without waste with a strong frictional force, the pulverization, mixing and dispersion are beautiful with the single peak particle diameter.<br>
