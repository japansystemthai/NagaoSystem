                  3D Ball Mill(3D Reactor) Q&A
                  <br>
                  ①What is the difference between a planetary ball mill and a 3D ball mill(３D Reactor)?<br>
A 3D ball mill(3D Reactor) is a ball mill having two longitudinal and transverse axes, like a gyroshin single.Since balls move in random directions by adjusting the rotation of the two axes, we have made it possible to efficiently crush using the whole container rather than a planetary ball mill.<br>
The possibility that crushing can not be done by the planetary ball mill can be increased, and the possibility of shortening the crushing time also increases.
Compared with the planetary ball mill, highly uniform crushing, mixing and dispersing have been made possible.
<br>
  <br>
②What is the difference between pulverization of planetary ball mill and 3D ball mill(３D Reactor)?<br>
In the 3D ball mill(3D Reactor), crushing balls and crushed material hit the entire container, and the crushing power exceeding Planet series and planetary ball mill is <br>realized.Furthermore, since the 3D ball mill(3D Reactor) moves around in the container at all times, solidification is hardly done especially when dry pulverizing.<br>
That is, there is a high possibility that the solidification problem at the time of dry milling, which was a weak point of the planetary ball mill, can be solved.<br>
Furthermore, It is good at mixing substances with different specific gravity and viscosity.
<br>
  <br>
Milling strength ⇒ futility without waste is a feature.Batch type, more finely crushed uniformly with single peak.Moreover, the edges of the particles after milling are round and keep the particle shape.
<br>
  <br>
Strength of mixing / dispersion ⇒ It is good at uniform mixture dispersion using the wall of the container without waste without using a propeller.
<br>
    <br>
③Are there consumables for 3D ball mill(３D Reactor)?
Rubber and bearings to be contacted during rotation.If you think of it as a car, think of wear exchange on the tire.The advantage of rubber contact is that the sound is quiet, the use of gears and gears is slight, and features of failure are few.
<br>
    <br>
④Is it safe without a cooling fan?
Unlike the Planet series and the planetary ball mill, the 3D ball mill milling and mixes using the entire container, so it is almost impossible to disperse the heat of crushing throughout the container and become hot.
<br>
    <br>
⑤Is it possible to investigate whether it can be crushed with a 3D ball mill(３D Reactor)?
Except for hazardous materials that have an effect on the human body, it is possible if it is a small amount (about 100 g or less).Please send a small sample (less than 100 g) or (Asaku-ku Kawasaki-city Kanagawa-Pref.)Odakyu Line Kakio Station in front of our exhibition room so we experiment with the engineers using various machines.It is possible to receive.Based on the experimental results, we believe that it will be easier to decide the model, container material and so on.I look forward to hearing from you.
<br>
    <br>
⑥What kind of materials can be crushed with the 3D ball mill(３D Reactor)?
If it can crush with a hammer, most things can be crushed.The one which can not be broken is plastic such as PET bottle, agar when it is food.However, it can be pulverized if hardened with liquid nitrogen or the like.Sticky metals such as Ag can be broken depending on the pulverization method, but in some cases it is larger than before pulverization.
<br>
    <br>
⑦Is it possible to milling organic matter with a 3D ball mil(３D Reactor)l?
Organic matter crushing is possible.The crushing of inorganic matter is the field of good grinding of planetary ball mill.However, it is difficult to crush organic matter due to heat of impact / frictional force.However, in case of 3D ball mill(3D Reactor), since the entire container surface is utilized, heat is dispersed and it is difficult to reach high temperature.Therefore it is also possible to crush organic substances and substances sensitive to heat.
<br>
    <br>
⑧What was the crushing experience of the 3D ball mill(３D Reactor)?
It is 10 nanometers with titanium oxide when wet.Pulverization to submicron class is possible if dry type.Furthermore, it crushes for a shorter time than the planetary ball mill.Moreover, it realized uniform pulverization of single peak.
<br>
  <br>
